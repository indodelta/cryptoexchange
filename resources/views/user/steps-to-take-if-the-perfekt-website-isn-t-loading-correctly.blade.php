								<div class="title">
									<h3>Steps to Take if the Perfektpay Website Isn't Loading Correctly</h3>
									<div class="meta">Last Updated: Feb 27, 2018 06:31PM UTC</div>
								</div>

								<div class="article-content">
									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">If the Perfektpay web page isn't loading correctly, please try clearing out your browser history settings. Instructions for various browsers are attached below. After following the steps for your browser, if you are still experiencing&nbsp;technical </span></span><span style="font-family: arial, helvetica, sans-serif; font-size: 14px;">difficulties, please contact our support team at <a href="#"><span style="color:#0000FF;">support@perfektpay.com</span></a>&nbsp;with "Technical Difficulty" as the subject. We are sorry for any inconveniences this may be causing you.</span></div>
									<div>&nbsp;</div>
									<div><strong><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Chrome</span></span></strong></div>
									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">In the browser bar, enter:</span></span></div>
									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">chrome://settings/clearBrowserData</span></span></div>
									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Select the following:</span></span></div>
									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Browsing history</span></span></div>
									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Download history</span></span></div>
									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Cookies and other site and plug-in data</span></span></div>
									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Cached images and files</span></span></div>
									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">From the Obliterate the following items from: drop-down menu, you can choose the period of time for which you want to clear cached information. To clear your entire cache, select the beginning of time.</span></span></div>
									<div>&nbsp;</div>
									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Click Clear browsing data.</span></span></div>
									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Exit/quit all browser windows and re-open the browser.</span></span></div>
									<div>&nbsp;</div>
									<div><strong><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Firefox</span></span></strong></div>
									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">From the History menu, select Clear Recent History.</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">If the menu bar is hidden, press Alt to make it visible.</span></span></div>

									<div>&nbsp;</div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">From the Time range to clear: drop-down menu, select the desired range; to clear your entire cache, select Everything.</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Next to "Details", click the down arrow to choose which elements of the history to clear; to clear your entire cache, select all items.</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Click Clear Now.</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Exit/quit all browser windows and re-open the browser.</span></span></div>

									<div>&nbsp;</div>

									<div>&nbsp;</div>

									<div><strong><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Microsoft Edge</span></span></strong></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">In the top right, click the Hub icon (looks like three horizontal lines).</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Click the History icon, and then select Clear all history.</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Select Browsing history, then Cookies and saved website data, and then Cached data and files. Click Clear.</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">After the "All Clear!" message appears, exit/quit all browser windows and re-open the browser.</span></span></div>

									<div>&nbsp;</div>

									<div>&nbsp;</div>

									<div><strong><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Internet Explorer 9 and higher</span></span></strong></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Note:</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">On January 12, 2016, Microsoft ended support for Internet Explorer versions prior to version 11 . UITS strongly recommends that you upgrade to a new operating system if your current system does not support Internet Explorer 11.</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Select Tools &gt; Safety &gt; Delete browsing history....</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">If the menu bar is hidden, press Alt to make it visible.</span></span></div>

									<div>&nbsp;</div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Deselect Preserve Favorites website data, and select:</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Temporary Internet files or Temporary Internet files and website files</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Cookies or Cookies and website data</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">History</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Click Delete. You will see a confirmation at the bottom of the window when the process is complete.</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Exit/quit all browser windows and re-open the browser.</span></span></div>

									<div>&nbsp;</div>

									<div><strong><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Opera</span></span></strong></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">From the Opera menu, select Settings, and then Delete Private Data....</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">In the dialog box that opens, select the items you want to clear, and then click Delete.</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Exit/quit all browser windows and re-open the browser.</span></span></div>

									<div>&nbsp;</div>

									<div><strong><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Safari</span></span></strong></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Safari 8</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">From the Safari menu, select Clear History and Website Data....</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Select the desired time range, and then click Clear History.</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Go to Safari &gt; Quit Safari or press Command-Q to exit the browser completely.</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Safari 7 and below</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">From the Safari menu, select Reset Safari....</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Select the items you want to reset, and then click Reset. As of Safari 5.1, Remove all website data includes both cookies and cache.</span></span></div>

									<div><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:14px;">Go to Safari &gt; Quit Safari or press Command-Q to exit the browser completely.<br>
